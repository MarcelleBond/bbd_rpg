package com.bbd.RPG.models.stompmessages;

public class InitializeMapIn {
    public Integer x;
    public Integer y;

    public InitializeMapIn(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }
}
